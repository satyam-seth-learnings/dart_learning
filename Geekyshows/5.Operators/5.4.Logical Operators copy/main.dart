// Logical Operators
void main() {
  /**
   * Logical AND (&&) Operator
   * 
   * true && true = true
   * true && false = false
   * false && true = false
   * false && false = false
   * 
   * Logical OR (||) Operator
   * 
   * true && true = true
   * true && false = true
   * false && true = true
   * false && false = false
   * 
   */

  var a = 100;
  var b = 200;
  var c = 300;
  var d = 400;

  print((a == b) && (c == d));
  print((a == b) || (c == d));
}
